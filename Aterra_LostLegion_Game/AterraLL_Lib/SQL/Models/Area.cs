public class Area {
    [PrimaryKey, AutoIncrement]
    public int Id { get; set; }
    
    [NotNull]
    public int MaxX { get; set; }
    
    [NotNull]
    public int MaxY { get; set; }
    
    [Unique, NotNull]
    public string Name { get; set; }
    
}