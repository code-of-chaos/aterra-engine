// ---------------------------------------------------------------------------------------------------------------------
using System.Xml.Serialization;
namespace AterraEngine.Items;

// ---------------------------------------------------------------------------------------------------------------------
// Code
// ---------------------------------------------------------------------------------------------------------------------
[XmlRoot("item")]
public class Item {
    [XmlAttribute("id")]
    public string itemId { get; } // maybe hex format of 0x000000 ?
    
    [XmlAttribute("text_resource")]
    public string textResource { get; }
    
    // -----------------------------------------------------------------------------------------------------------------
    // Constructor
    // -----------------------------------------------------------------------------------------------------------------
    public Item(string item_id, string text_resource) {
        itemId = item_id;
        textResource = text_resource;
    }
    
}